# ---------------- Stylometry calculations ---------------- #

import re, math, pandas as pd, collections

# Sentence segmentation (naïve).
sentences = re.split(r'(?<=[.!?])\s+', chapters_text.strip())
sentences = [s for s in sentences if s]

# Word tokenisation (simple alpha tokens).
word_tokens = []
for s in sentences:
    word_tokens.extend(re.findall(r"[A-Za-z']+", s.lower()))

total_words = len(word_tokens)
unique_words = len(set(word_tokens))
type_token_ratio = unique_words / total_words if total_words else 0

# Sentence-length stats.
sent_lengths = [len(re.findall(r"[A-Za-z']+", s)) for s in sentences]
mean_len = sum(sent_lengths) / len(sent_lengths)
std_len = (sum((l - mean_len) ** 2 for l in sent_lengths) / len(sent_lengths)) ** 0.5
burstiness = std_len / mean_len

# Shannon entropy (unigram).
freqs = collections.Counter(word_tokens)
entropy = -sum((c / total_words) * math.log2(c / total_words) for c in freqs.values())

# Adverbial sentence openers ("ly" rule of thumb).
ly_openers = sum(1 for s in sentences if re.match(r"\s*\b\w+ly\b", s.strip(), flags=re.I))
ly_ratio = ly_openers / len(sentences)

# Assemble results.
results = pd.DataFrame(
    {"Metric": [
        "Total words", "Unique words", "Type–token ratio",
        "Mean sentence length", "Std sentence length", "Burstiness (std/mean)",
        "Shannon entropy (bits/word)",
        "Sentences starting with *-ly* word", "Ratio of *-ly* openers"
    ],
     "Value": [
         total_words, unique_words, round(type_token_ratio, 3),
         round(mean_len, 2), round(std_len, 2), round(burstiness, 3),
         round(entropy, 2),
         ly_openers, round(ly_ratio, 3)
     ]}
)

# Display results.
print(results.to_string(index=False))
